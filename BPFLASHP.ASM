Read2K

	LACC 	DPA0
	AND  	#01Fh
	ADD  	FLADR+1,6
	SACL 	DPA0
	OUT  	DPA0,PA0
	OR   	#20h
	SACL 	DPA0

	LACC    FLADR
	OR	#08000H
	SACL	TMP
	LAR  	AR7,TMP
	LAR	AR0,#07FFH
	MAR	*,AR7
Read2K_1
	LACC	*+,0,AR2
	SACL	*+,0,AR0
	BANZ    Read2K_1,*-,AR7
	OUT  	DPA0,PA0
	RET

