	BCND 	FL_NoOver,NEQ
	SACL	FLADR
	LACC 	FLADR+1
	ADD  	#1
	SACL 	FLADR+1
FL_NoOver
	LAR  	AR2,#0D555h
	LAR  	AR3,#0AAAAh
	LAR  	AR4,TMP+1      ; PA
	MAR  	*,AR2
	SPLK 	#0AAh,*,AR3
	SPLK 	#055h,*,AR2
	SPLK 	#0A0h,*,AR4
	BLDD 	#TMP,*,AR2     ; PD из TMP во FLASH
	LACC 	#0FFh
FLPRG1  SUB  	#1
	RPT  	#1
	NOP
	BCND 	ERRPRG,LEQ
	BCND 	FLPRG1,BIO
	SPLK 	#0AAh,*,AR3
	SPLK 	#055h,*,AR2
	SPLK 	#0F0h,*,AR4
	BLDD 	*,#TMP+1,AR2   ; PD из FLASH в
	OUT  	DPA0,PA0
