        OR      #0020H      ;выключаем выборку флеш #CEF =1 (#CE)
        SACL    DPA0       ;запоминаем состояние порта в памяти
        SPLK    #0AAh,*,AR3   ;программируем флеш на стирание
        SPLK    #055h,*,AR2
        SPLK    #080h,*,AR2
        SPLK    #0AAh,*,AR3
        SPLK    #055h,*,AR2
        SPLK    #030h,*,AR2
        LACC    #07FFFh            ; T = 15 с.
	SPLK    #0800h,TMP+1
FLCLR2  SUB     #1
